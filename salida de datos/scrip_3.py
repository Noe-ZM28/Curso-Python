#Mostrar informacion en pantalla

print ("{:>30}".format("palabra")) #mostrar texto 30 caracteres a la derecha

print ("{:^30}".format("palabra"))# mostrar texto alineadi al centro de 30 caracteres a la derecha

print ("{:.3}".format("palabra"))# mostrar texto solo hasta la letra indicada

print ("{:^30.3}".format("palabra"))# mostrar texto alineadi al centro de 30 caracteres a la derecha solo hasta la letra indicada
